// Solicita que o usuário insira o primeiro número e armazena na variável num1
var num1 = parseFloat(prompt("Digite o primeiro número:"));

// Solicita que o usuário insira o segundo número e armazena na variável num2
var num2 = parseFloat(prompt("Digite o segundo número:"));

// Calcula a soma dos dois números
var soma = num1 + num2;

// Exibe o resultado da soma em um alert
alert("A soma dos números é: " + soma);
